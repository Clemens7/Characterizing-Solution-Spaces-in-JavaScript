export class Artwork {


    /**
     *
     * @param objectID
     * @param title
     * @param artist
     * @param image
     * @param date
     */
    constructor(objectID, title, artist, image, date) {
        this.objectID = objectID;
        this.title = title;
        this.artist = artist;
        this.image = image;
        this.date = date;
    }

}